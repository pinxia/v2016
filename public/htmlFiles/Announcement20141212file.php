            <p class="lead">Dear SCCS Students, Teachers, and Parents,</p>

            <p>Another exciting semester has flown by. This Sunday, December 14, is the last day of school for the fall 2014 semester. We hope that you and your child have had a wonderful experience at our school. We are grateful for the opportunity to serve your child.</p>

            <p>To ensure everything running smoothly on the last day of school, I would like to draw you attention on a few important things:</p>
             
                <p><b>§  First,</b> the School is organizing an end-of-semester talent show from 12:30 to 1:40 P.M. in Room C112 to celebrate the achievement of our students. In order to accommodate this event, all classes will start 20 min later than the regular class schedule. As a result, the whole class period will run from 1:40 to 5:10 P.M. instead of 1:20 to 4:50 P.M. this Sunday. More details about the talent show will be sent out shortly by the PTA.</p>

                <p><b>§  Second,</b> please remember to return the teaching evaluation forms to the school office located in Room A105. If, for some reason, you haven’t received the evaluation form, please download the attached electronic copy. Your feedback and suggestions are crucial to improve our teaching quality. We would greatly appreciate if you can take a couple of minutes to complete the form.</p>

                <p><b>§  The school store will be open this Sunday.</b> Please remind your child to come to the cafeteria to redeem prizes. Please note that there is an important change of policy: gift cards are no longer available at the store. Students must come to the office to redeem gift cards. Students can get gift cards throughout the semester whenever the office is open. Please inform your child about this important change.</p>

                <p><b>§  Last but not least,</b> a quick reminder that the spring 2015 semester will start on Sunday, January 18 (Chinese classes start on that day and enrichment classes start one week later). All school dates can be found in “School Calendar” on our website. If you haven’t registered for the spring semester, please contact our IT managers at: <a href="mailto:support@ynhchineseschool.org">support@ynhchineseschool.org</a> They will help you register classes or make adjustment.</p>
            
            <p>Once again, thank you very much for such a wonderful semester at the SCCS. My colleagues and I really enjoyed working with you all. It is truly rewarding.</p>

            <p>Best wishes for a wonderful holiday season and a New Year filled with Peace and Happiness!</p>

            <p>Cordially,</p>

            <p>Henan Cheng, Ed.D.</p>
